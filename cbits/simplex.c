inline float
dot(float x, float y, float z, int idx)
{
  return x * grad[idx][0] + y * grad[idx][1] + z * grad[idx][2];
}

float
simplexNoise(float xin, float yin, float zin)
{
  float f3 = 1.0 / 3.0;
  float g3 = 1.0 / 6.0;
  float s = (xin + yin + zin) * f3;

  int i = xin + s;
  int j = yin + s;
  int k = zin + s;

  float t = (i + j + k) * g3;
  float x0 = xin - i + t;
  float y0 = yin - j + t;
  float z0 = zin - k + t;

  int i1, j1, k1, i2, j2, k2;
  if (x0 >= y0)
  {
    if (y0 >= z0)
    {
      i1 = 1; j1 = 0; k1 = 0;
      i2 = 1; j2 = 1; k2 = 0;
    }
    else if (x0 >= z0)
    {
      i1 = 1; j1 = 0; k1 = 0;
      i2 = 1; j2 = 0; k2 = 1;
    }
    else
    {
      i1 = 0; j1 = 0; k1 = 1;
      i2 = 1; j2 = 0; k2 = 1;
    }
  }
  else
  {
    if (y0 < z0)
    {
      i1 = 0; j1 = 0; k1 = 1;
      i2 = 0; j2 = 1; k2 = 1;
    }
    else if (x0 < z0)
    {
      i1 = 0; j1 = 1; k1 = 0;
      i2 = 0; j2 = 1; k2 = 1;
    }
    else
    {
      i1 = 0; j1 = 1; k1 = 0;
      i2 = 1; j2 = 1; k2 = 0;
    }
  }

  float x1 = x0 - i1 + g3;
  float y1 = y0 - j1 + g3;
  float z1 = z0 - k1 + g3;
  float x2 = x0 - i2 + 2.0 * g3;
  float y2 = y0 - j2 + 2.0 * g3;
  float z2 = z0 - k2 + 2.0 * g3;
  float x3 = x0 - 1.0 + 3.0 * g3;
  float y3 = y0 - 1.0 + 3.0 * g3;
  float z3 = z0 - 1.0 + 3.0 * g3;

  int ii = i & 0xFF;
  int jj = j & 0xFF;
  int kk = k & 0xFF;

  int gi0 = permMod12[ii + perm[jj + perm[k]]];
  int gi1 = permMod12[ii + i1 + perm[jj + j1 + perm[k + k1]]];
  int gi2 = permMod12[ii + i2 + perm[jj + j2 + perm[k + k2]]];
  int gi3 = permMod12[ii +  1 + perm[jj +  1 + perm[k +  1]]];

  float t2, n0 = 0.0f, n1 = 0.0f, n2 = 0.0f, n3 = 0.0f;
  if ((t = 0.6 - x0 * x0 - y0 * y0 - z0 * z0) >= 0.0)
  {
    t2 = t * t;
    n0 = t2 * t2 * dot(x0, y0, z0, gi0);
  }

  if ((t = 0.6 - x1 * x1 - y1 * y1 - z1 * z1) >= 0.0)
  {
    t2 = t * t;
    n1 = t2 * t2 * dot(x1, y1, z1, gi1);
  }

  if ((t = 0.6 - x2 * x2 - y2 * y2 - z2 * z2) >= 0.0)
  {
    t2 = t * t;
    n2 = t2 * t2 * dot(x2, y2, z2, gi2);
  }

  if ((t = 0.6 - x3 * x3 - y3 * y3 - z3 * z3) >= 0.0)
  {
    t2 = t * t;
    n3 = t2 * t2 * dot(x3, y3, z3, gi3);
  }

  return 32 * (n0 + n1 + n2 + n3);
}